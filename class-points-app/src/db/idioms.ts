export async function ensureBuiltinIdioms(): Promise<void> {
  const db = await getDB()
  const count = await db.count('idioms')
  if (count === 0) {
    const seen = new Set<string>()
    for (const text of builtinIdioms) {
      const t = text.trim()
      if (!t || seen.has(t)) continue
      seen.add(t)
      await db.add('idioms', { id: id(), text: t })
    }
  }
}

export async function searchIdiomByFirstChar(firstChar: string): Promise<Idiom[]> {
  const db = await getDB()
  await ensureBuiltinIdioms()
  const all = await db.getAll('idioms')
  return all.filter((i) => i.text.charAt(0) === firstChar)
}

export async function hasIdiom(text: string): Promise<boolean> {
  const db = await getDB()
  await ensureBuiltinIdioms()
  const all = await db.getAll('idioms')
  return all.some((i) => i.text === text)
}

export async function addCustomIdiom(text: string): Promise<Idiom> {
  const db = await getDB()
  const idiom: Idiom = { id: id(), text: text.trim() }
  await db.add('idioms', idiom)
  return idiom
}

export async function addIdiomChainRecord(
  studentId: string,
  idiom: string,
  prevIdiom: string,
  pointsAwarded?: number
): Promise<IdiomChainRecord> {
  const db = await getDB()
  const r: IdiomChainRecord = {
    id: id(),
    studentId,
    idiom,
    prevIdiom,
    at: Date.now(),
    pointsAwarded
  }
  await db.add('idiomChainRecords', r)
  return r
}

export async function listIdiomChainRecords(limit = 50): Promise<IdiomChainRecord[]> {
  const db = await getDB()
  const list = await db.getAllFromIndex('idiomChainRecords', 'by-at')
  return list.sort((a, b) => b.at - a.at).slice(0, limit)
}
